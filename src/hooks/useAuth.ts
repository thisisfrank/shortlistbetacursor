import { useState, useEffect } from 'react';
import { User } from '@supabase/supabase-js';
import { supabase } from '../lib/supabase';

export interface UserProfile {
  id: string;
  email: string;
  role: 'client' | 'sourcer' | 'admin';
  created_at: string;
  updated_at: string;
}

export const useAuth = () => {
  const [user, setUser] = useState<User | null>(null);
  const [userProfile, setUserProfile] = useState<UserProfile | null>(null);
  const [loading, setLoading] = useState(true);
  const [signOutLoading, setSignOutLoading] = useState(false);

  useEffect(() => {
    // Get initial session
    const getInitialSession = async () => {
      console.log('üîç Getting initial session...');
      
      try {
        const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
        const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY;
        
        console.log('üîó Supabase URL:', supabaseUrl);
        console.log('üîë Has Anon Key:', !!supabaseAnonKey);
        
        // Check if environment variables are missing
        if (!supabaseUrl || !supabaseAnonKey) {
          console.error('‚ùå Missing Supabase environment variables');
          setUser(null);
          setUserProfile(null);
          setLoading(false);
          return;
        }
        
        const { data: { session }, error: sessionError } = await supabase.auth.getSession();
        console.log('üì° Session response received:', { hasSession: !!session, error: sessionError });
        
        if (sessionError) {
          console.error('‚ùå Error getting session:', sessionError);
          setUser(null);
          setUserProfile(null);
          setLoading(false);
          return;
        }
        
        const currentUser = session?.user ?? null;
        console.log('üë§ Initial session user:', currentUser?.email || 'No user');
        
        setUser(currentUser);
        
        if (currentUser) {
          console.log('üìã User found, fetching profile...');
          
          try {
            // Add timeout to prevent hanging
            // Try a simpler query first to debug the issue
            console.log('üîç Querying profile with ID:', currentUser.id);
            
            const profilePromise = supabase
              .from('user_profiles')
              .select('id, email, role, created_at, updated_at')
              .eq('id', currentUser.id)
              .maybeSingle();
            
            const timeoutPromise = new Promise((_, reject) => 
              setTimeout(() => reject(new Error('Profile fetch timeout')), 30000)
            );
            
            const { data: profile, error } = await Promise.race([profilePromise, timeoutPromise]) as any;
            
            console.log('üìä Profile query result:', { hasProfile: !!profile, error: error });
            
            if (error) {
              console.error('‚ö†Ô∏è Error fetching user profile:', error);
              console.log('üîç Error details:', { code: error.code, message: error.message, details: error.details });
              
              // Log all errors for debugging since we know the profile exists
              console.log('üîç All error details:', { 
                code: error.code, 
                message: error.message, 
                details: error.details,
                hint: error.hint 
              });
              
              // For now, let's just set the profile to null and continue
              // This will allow the user to proceed while we debug the query issue
              setUserProfile(null);
              
              setUserProfile(null);
            } else {
              if (profile) {
                console.log('‚úÖ User profile found:', profile);
                const userProfile: UserProfile = {
                  id: profile.id,
                  email: profile.email,
                  role: profile.role,
                  created_at: profile.created_at,
                  updated_at: profile.updated_at
                };
                setUserProfile(userProfile);
              } else {
                console.log('‚ö†Ô∏è No user profile found, waiting for trigger to create it');
                setUserProfile(null);
              }
            }
          } catch (profileError) {
            console.error('üí• Profile fetch failed:', profileError);
            setUserProfile(null);
          }
        } else {
          console.log('üö´ No user session found');
          setUserProfile(null);
        }
      } catch (error) {
        console.error('üí• Error in getInitialSession:', error);
        setUser(null);
        setUserProfile(null);
      }
      
      setLoading(false);
      console.log('üèÅ Auth initialization complete');
    };

    getInitialSession();

    // Listen for auth changes
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log('üîÑ Auth state change:', event);
        const currentUser = session?.user ?? null;
        setUser(currentUser);
        
        if (currentUser) {
          console.log('üë§ Auth change - fetching profile for:', currentUser.email);
          
          try {
            // Add timeout for auth state changes too
            // Try a simpler query first to debug the issue
            console.log('üîç Auth change - Querying profile with ID:', currentUser.id);
            
            const profilePromise = supabase
              .from('user_profiles')
              .select('id, email, role, created_at, updated_at')
              .eq('id', currentUser.id)
              .maybeSingle();
            
            const timeoutPromise = new Promise((_, reject) => 
              setTimeout(() => reject(new Error('Profile fetch timeout')), 30000)
            );
            
            const { data: profile, error } = await Promise.race([profilePromise, timeoutPromise]) as any;
            
            if (error) {
              console.error('‚ö†Ô∏è Auth change - Error fetching user profile:', error);
              console.log('üîç Auth change - Error details:', { code: error.code, message: error.message, details: error.details });
              
              // Log all errors for debugging since we know the profile exists
              console.log('üîç Auth change - All error details:', { 
                code: error.code, 
                message: error.message, 
                details: error.details,
                hint: error.hint 
              });
              
              // For now, let's just set the profile to null and continue
              // This will allow the user to proceed while we debug the query issue
              setUserProfile(null);
              
              setUserProfile(null);
            } else {
              if (profile) {
                console.log('‚úÖ Auth change - Profile found:', profile);
                const userProfile: UserProfile = {
                  id: profile.id,
                  email: profile.email,
                  role: profile.role,
                  created_at: profile.created_at,
                  updated_at: profile.updated_at
                };
                setUserProfile(userProfile);
              } else {
                console.log('‚ö†Ô∏è Auth change - No profile found, waiting for trigger');
                setUserProfile(null);
              }
            }
          } catch (profileError) {
            console.error('üí• Auth change profile fetch failed:', profileError);
            setUserProfile(null);
          }
        } else {
          console.log('üö´ Auth change - No user');
          setUserProfile(null);
        }
        setLoading(false);
      }
    );

    return () => subscription.unsubscribe();
  }, []);

  const signIn = async (email: string, password: string) => {
    try {
      console.log('üîê Attempting sign in for:', email);
      setLoading(true);
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email: email.trim(),
        password,
      });
      
      console.log('üîê Sign in result:', { success: !error, error: error?.message });
      
      // Don't set loading to false here - let the auth state change handle it
      // This prevents the flickering issue
      
      return { data, error };
    } catch (error) {
      console.error('üí• Sign in catch error:', error);
      setLoading(false); // Only set loading to false on error
      return { 
        data: null, 
        error: { message: 'Network error. Please try again.' } 
      };
    }
  };

  const signUp = async (email: string, password: string, role: 'client' | 'sourcer' = 'client') => {
    console.log('üìù Attempting signup with:', { email, passwordLength: password.length, role });
    setLoading(true);
    
    try {
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          emailRedirectTo: undefined
        },
      });
      
      console.log('üìù Signup result:', { success: !error, error: error?.message, hasUser: !!data.user });
      
      if (!error && data.user) {
        console.log('üë§ User created, updating profile role...');
        
        try {
          // Wait a moment for the trigger to create the profile
          await new Promise(resolve => setTimeout(resolve, 1000));
          
          // Update user profile with selected role (the profile is auto-created by trigger)
          const { error: profileError } = await supabase
            .from('user_profiles')
            .update({ role })
            .eq('id', data.user.id);
          
          if (profileError) {
            console.error('‚ö†Ô∏è Error updating user profile:', profileError);
          } else {
            console.log('‚úÖ User profile role updated successfully');
          }
        } catch (profileUpdateError) {
          console.error('üí• Error in profile update:', profileUpdateError);
        }
      }
      
      return { data, error };
    } catch (error) {
      console.error('üí• Signup error:', error);
      return { 
        data: null, 
        error: { message: 'Network error. Please try again.' } 
      };
    } finally {
      setLoading(false);
    }
  };

  const signOut = async () => {
    console.log('üö™ Signing out...');
    
    // Set sign-out loading state
    setSignOutLoading(true);
    
    try {
      const { error } = await supabase.auth.signOut();
      console.log('üö™ Supabase signOut result:', { error });
      
      // Clear local state after successful sign-out
      setUser(null);
      setUserProfile(null);
      setLoading(false);
      localStorage.removeItem('sourcerName');
      localStorage.removeItem('savedSourcers');
      
      return { error };
    } catch (error) {
      console.error('üí• Sign out error:', error);
      return { error: { message: 'Error signing out' } };
    } finally {
      setSignOutLoading(false);
    }
  };

  return {
    user,
    userProfile,
    loading,
    signOutLoading,
    signIn,
    signUp,
    signOut,
  };
};